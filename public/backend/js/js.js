function getSlug(str) {
    const mapSpecialCharacters = {
        'á': 'a', 'à': 'a', 'ả': 'a', 'ã': 'a', 'ạ': 'a',
        'ă': 'a', 'ắ': 'a', 'ằ': 'a', 'ẳ': 'a', 'ẵ': 'a', 'ặ': 'a',
        'â': 'a', 'ấ': 'a', 'ầ': 'a', 'ẩ': 'a', 'ẫ': 'a', 'ậ': 'a',
        'é': 'e', 'è': 'e', 'ẻ': 'e', 'ẽ': 'e', 'ẹ': 'e',
        'ê': 'e', 'ế': 'e', 'ề': 'e', 'ể': 'e', 'ễ': 'e', 'ệ': 'e',
        'í': 'i', 'ì': 'i', 'ỉ': 'i', 'ĩ': 'i', 'ị': 'i',
        'ó': 'o', 'ò': 'o', 'ỏ': 'o', 'õ': 'o', 'ọ': 'o',
        'ô': 'o', 'ố': 'o', 'ồ': 'o', 'ổ': 'o', 'ỗ': 'o', 'ộ': 'o',
        'ơ': 'o', 'ớ': 'o', 'ờ': 'o', 'ở': 'o', 'ỡ': 'o', 'ợ': 'o',
        'ú': 'u', 'ù': 'u', 'ủ': 'u', 'ũ': 'u', 'ụ': 'u',
        'ư': 'u', 'ứ': 'u', 'ừ': 'u', 'ử': 'u', 'ữ': 'u', 'ự': 'u',
        'ý': 'y', 'ỳ': 'y', 'ỷ': 'y', 'ỹ': 'y', 'ỵ': 'y',
        'đ': 'd',
        ' ': '-', '_': '-', // Replace spaces and underscores with hyphens
    };

    str = str.replace(/[^\w\s]/gi, function (char) {
        return mapSpecialCharacters[char] || char;
    });

    // Replace spaces with hyphens
    str = str.replace(/\s+/g, '-');

    // Remove consecutive hyphens
    str = str.replace(/-{2,}/g, '');

    // Convert to lowercase
    str = str.toLowerCase();

    return str;
}

const title = document.querySelector('.title');
const slug = document.querySelector('.slug');
title.addEventListener('keyup', (e) => {
    const titleValue = e.target.value;
    slug.value = getSlug(titleValue);
})

slug.addEventListener('change', (e) => {
    if (slug.value == '') {
        const title = document.querySelector('.title');
        const titleValue = title.value;
        slug.value = getSlug(titleValue);
    }
})

